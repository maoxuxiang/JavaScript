				Case 2 - <span class="diseasetext" value="DM">Diabetes
					Mellitus</span>
			</h3>
			<div>
				<p>Agnes is a 51-year-old female with hypertension who received a diagnosis of type 2 diabetes a decade ago. She has been worried about her diabetes since then because she has not been able to gain complete control over it. Her glycated hemoglobin level was 7.0% for 1 year but gradually increased to 9.0%. For the past 2 years, she has been taking metformin 2000 mg daily. She is maintaining her weight at 165 pounds (75 kg), but she is not able to lose weight. Agnes goes to the gym and walks on a treadmill three times a week. She tells you that she has made as many lifestyle changes as she can. Agnes's hypertension is well controlled with an ACE inhibitor; she also takes a statin. Her most recent tests showed LDL cholesterol of 85 mg / dL and HDL of 62 mg / dL.</p>
				<p>Agnes hates needles and won't use insulin. Her sister, who also has diabetes, was receiving glipizide but had episodes of hypoglycemia while taking that drug, including one episode that resulted in an auto accident. Agnes is also worried about weight gain associated with that drug. Her sister recently switched from glipizide to saxagliptin and reports to Agnes that she has had no further episodes of hypoglycemia. Agnes has also heard about a new class of diabetes drugs that works by eliminating excess glucose through the urine. She wants to know about the safety of the newer drugs and asks specifically about saxagliptin and the new class of drugs. You explain to her that the drugs she is asking about are in different classes - dipeptidyl peptidase 4 (DPP-4) inhibitors ("gliptins") and sodium glucose cotransporter 2 (SGLT2) inhibitors ("gliflozins").</p>
				<p>Do you think a second drug should be added to the metformin Agnes is currently receiving? If so, what drug?</p>
