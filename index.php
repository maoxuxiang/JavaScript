					with prostitutes. He had not always used a condom and his last
					unprotected sexual encounter occurred about three days previously.
					The patient's illness history confirmed the presence of urethral
					purulent discharge, staining the slip. Urination was painful,
					especially the first one in the morning. Systemic symptoms (fever,
					chills, sweats, nausea) and testicle pain were absent. The physical
					examination confirmed the presence of urethral discharge, testicles
					were normal, there was no inguinal adenopathy nor genital ulcer,
					the mouth, pharynx and tonsils showed no anomalies. Urethral swab
					and urine tests for N. gonorrheae and C. trachomatis were ordered.</p>
