					rheumatoid arthritis (RA), class II heart failure (HF), and
					hypertension. His RA has been managed with methotrexate (MTX) for 7
					months, without complete remission. His current dose of MTX was
					titrated during this period, starting at 7.5 mg weekly towards 25
					mg weekly. He has also been taking prednisone 15 mg/day during the
					same period. His HF and hypertension have been well-controlled with
					lisinopril 15 mg / day and carvedilol 25 mg twice daily. He
					presents to the office today because his pain on wrists, hands, and
					feet has gotten worse. He has been receiving advertisements in the
					mail from manufacturers of a variety of other medications for
					rheumatoid arthritis and he wonders if he might benefit. However,
					reading through the potential side effects (cancer, sudden death),
					he is hesitant to try these medications if there is no clear
					benefit.</p>
				<p>Would you change John's treatment strategy? If so, how?</p>
