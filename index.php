			<h3>
				Case 3 - <span class="diseasetext" value="VUR">Vesicoureteral Reflux</span>
			</h3>
			<div>
				<p>A 6-year-old girl was recently referred to your clinic for further evaluation and management of vesicoureteral reflux, which had first been discovered after she presented at 1 year of age with a temperature of 39.5°C and irritability. Culture of a urine specimen at that time showed more than 106 colony-forming units of pansensitive Escherichia coli per milliliter, and she was treated with intravenous ampicillin for several days, followed by oral ampicillin, for a total of 14 days of therapy. After the patient no longer had a fever and a urine culture was sterile, voiding cystourethrography was performed while the patient was still receiving ampicillin. The voiding cystourethrogram showed bilateral grade III vesicoureteral reflux, and renal ultrasonography revealed normal findings. Radionuclide renal scintiscanning was not performed.</p>
				
				<p>Prophylactic trimethoprim–sulfamethoxazole was administered at bedtime each night, and during the ensuing year, the patient had no urinary tract infections, as judged by sterile surveillance cultures of urine specimens and sterile cultures when she had any febrile episodes. A follow-up radionuclide voiding cystourethrogram when the patient was 2.5 years of age still showed bilateral grade III vesicoureteral reflux.</p>
				
				<p>Subsequently, the family moved a number of times. The patient had several episodes of high fever, but no urine cultures were documented. At 4 years of age, she was seen again at the center where she had been followed, and she still had bilateral grade III vesicoureteral reflux. Continued antibiotic prophylaxis was recommended, but because of the family's moves, the degree of adherence was unclear. However, the child remained well, and her parents decided to stop the prophylactic antibiotics.</p>
				
				<p>The child was toilet trained at 2 years of age, and she had no history of constipation or bowel irregularities. She had occasional nocturnal enuresis until the age of 4. She has not had enuresis since that time. Studies at the center where she had been followed documented measurements of serum creatinine of 0.3 mg per deciliter (27 μmol per liter) and blood urea nitrogen of 11 mg per deciliter (3.9 mmol per liter). Family history includes no chronic kidney disease, although the child's mother had a urinary tract infection when pregnant with this child, and the child's maternal grandmother, who is 51 years old, has had hypertension.</p>
				
				<p>The patient, now 6 years of age, and her family have recently moved to your city, and her new primary care pediatrician refers her to you for consultation. She has been well recently, without reports of any medical problems. On examination of the child, you find that the height and weight are at the 50th percentile for age and the blood pressure is 88/50 mm Hg. Results of a general physical examination, including examination of the external genitalia, are normal. You obtain a urine specimen; culture of the specimen shows fewer than 1000 colony-forming units. A urinalysis reveals a specific gravity of 1.018 and a pH of 6.0; urine dipstick testing is negative for leukocytes, nitrites, protein, blood, glucose, ketones, and urobilinogen. The sediment shows no bacteria, 0 to 1 white cells, no red cells, and no casts per high-power field. You also obtain a radionuclide voiding cystourethrogram, which reveals that the patient still has bilateral grade III reflux. A renal ultrasound study shows that the kidney size is normal. A radionuclide renal scan is also normal. The parents ask whether you think the patient needs antibiotic prophylaxis and whether you would recommend any procedure to stop the vesicoureteral reflux.</p>
				
				<p>Which one of the following approaches would you find most appropriate for this patient?</p>
				<ul class="circletype">
					<li>Option 1: Watchful Waiting without Antibiotics</li>
					<li>Option 2: Continuous Antimicrobial Prophylaxis</li>
					<li>Option 3: Repair of the Vesicoureteral Reflux</li>
				</ul>
