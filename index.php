					after walking more than one block. The other day, per his usual
					pattern, he walked to the near by grocery store but found that,
					once there, he did not have the energy or strength to walk back.
					His wife reports some recent cognitive changes in her husband, such
					as forgetting his daily vitamins and having difficulty organizing
					his daily activities. His medical history is significant for a GI
					bleed 5 years ago secondary to several small angiodysplasias that
					required brief hospitalization. He takes no medications and has no
					allergies. On physical exam he is noted to have an irregularly
					irregular heart rate of 130, blood pressure 150/80, and an ECG
					consistent with AF. He has normal vesicular breath sounds without
					rales, rhonchi, or wheezes; his extremities are without edema; and
					he has no focal neurologic findings.</p>
				<p>What is the next best step in his management?</p>
