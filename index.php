			<div>
				<p>A 68-year-old man who recently moved to the area presents to the
					urgent care clinic with an acute complaint of productive cough. He
					has a 20-pack year history of smoking but quit five years ago. He
					started last year on an albuterol inhaler by his prior physician.
					On further review, he has had no fevers, sweats, weight loss,
					nausea, or vomiting. He reports that for the past year he typically
					begins each morning with productive cough but is fine the rest of
					the day. This pattern is somewhat worse over the past two weeks,
					with his cough persisting for most of the morning. On examination
					he is a thin man in no acute distress. He is afebrile, breathing
					comfortably, with decreased breath sounds but no focal wheezes or
					areas of consolidation on pulmonary examination. The rest of his
					examination is within normal limits.</p>
				<p>What would be your next step in the management of this patient?</p>
				<ul class="circletype">
					<li>Prescribe a macrolide or cephalosporin antibiotic</li>
					<li>Initiate a 21-day tapered oral glucocorticoid regimen</li>
					<li>Initiate a long-acting beta2-agonist</li>
					<li>Admit the patient to the hospital</li>
				</ul>
				<p>Which of the following studies would you use in his long-term
					management?</p>
				<ul class="circletype">
					<li>Chest x-ray</li>
					<li>High-resolution chest CT</li>
					<li>Spirometry</li>
					<li>Sleep Study</li>
				</ul>
