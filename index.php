			<h3>
				Case 6.1 - <span class="diseasetext" value="">Congestive Heart
					Failure - Simvastatin</span>
			</h3>
			<div>
				<p>Mr. Peters, male, 72 years of age visits your practice because he increasingly experiences shortness of breath during exercise. He has no chest pain.  His past medical history is significant for hypertension since 1988, and an anterior wall myocardial infarction in 2001.  He takes chlorthalidone 12.5 mg q.d.; enalapril 5 mg b.i.d.; acetylsalicylic acid 80 mg q.d.; simvastatin 40 mg q.d.</p>
				<p>On physical exam his blood pressure is 146/87 mmHg, pulse 92 bpm regular, a broadened and sustained apical impulse in left decubital position, and normal pulmonary breathing sounds.</p>
				<p>Additional tests reveal plasma amino-terminal B-type Natriuretic Peptide (NT-proBNP) level 1010 pg/ml (completely normal when <125 pg/ml ≈ 15 pmol/l).  ECG shows abnormal Q-waves suggestive for prior MI.  Echocardiography shows a somewhat dilated heart with wall movement abnormalities compatible with a prior MI, and a left ventricular ejection fraction (LVEF) of 30%. No clinical relevant valvular disease.</p>
				<p>Conclusion: Heart failure with a reduced ejection fraction caused by long-term HT and prior MI.</p>
				<p>Do you continue or stop prescribing simvastatin?</p>
				<ul class="circletype">
					<li>Continue</li>
					<li>Continue, but only if the patient would have heart failure with preserved ejection fraction</li>
					<li>Continue, but only if the patient had HF-REF and a history of ischemic heart disease</li>
					<li>Stop</li>
				</ul>
			</div>
			
			
