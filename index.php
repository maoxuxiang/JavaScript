				<p>Conclusion: Heart failure with a reduced ejection fraction caused by long-term HT and prior MI.</p>
				<p>During the last consultation you decided to add metoprolol succinate 12.5 mg q.d. with the intention to gradually increase the dosage of this β-blocker up to the maximal tolerated dosage over the coming weeks ('starting low, going slow').</p>
				<p>We are four weeks later now, and Mr. Peters returns to the surgery for his next appointment. When asked for, he tells you that he tolerates the last prescribed drug well (metoprolol succinate; you have already increased the dosage to 25 mg o.d. two weeks ago), and does not experience any side effects, although, it seems somewhat harder to 'get going', and after his 'walking block' he feels somewhat more tired than before the start of metoprolol.  On physical examination, his blood pressure is 122/72 mmHg and his pulse is 72 bpm regular. No weight gain, no ankle edema and normal pulmonary breathing sounds.</p>
				<p></p>
				<p>Do you want to change his medication?</p>
				<ul class="circletype">
					<li>Yes, double the &beta;-blocker dosage</li>
					<li>No, no increase of &beta;-blocker dosage now</li>
					<li>Yes, temporarily increase the dosage of furosemide</li>
					<li>Yes, reduce metoprolol to 12.5 mg</li>
					<li>Yes, stop metoprolol</li>
				</ul>
			</div>
